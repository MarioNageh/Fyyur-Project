class ShowForm(FlaskForm):
    artist_id = QuerySelectField(
        'artist_id', validators=[DataRequired()],query_factory=lambda: Artist.query.all(),
        allow_blank=False,
        get_label='name', get_pk=lambda a: a.id,
    )
    venue_id= QuerySelectField(
        'venue_id', validators=[DataRequired()],query_factory=lambda: Venue.query.all(),
        allow_blank=False,
        get_label='name', get_pk=lambda a: a.id,
    )
    start_time = DateTimeField(
        'start_time',
        validators=[DataRequired()],
        default= datetime.today()
    )

